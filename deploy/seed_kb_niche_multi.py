# Запись в Supabase
# ===========================================================================

def run(dry_run: bool = False):
    from supabase import create_client

    url = os.environ["SUPABASE_URL"]
    key = os.environ["SUPABASE_SERVICE_KEY"]
    db  = create_client(url, key)

    print(f"\n📚 Всего записей для вставки: {len(ENTRIES)}\n")

    ok = 0
    for e in ENTRIES:
        key = e["entry_key"]
        if dry_run:
            print(f"  [dry] {key}: {e['title']}")
            ok += 1
            continue
        try:
            db.table("kb_entries").delete().eq("entry_key", key).execute()
            db.table("kb_entries").insert(e).execute()
            print(f"  ✓ {key}")
            ok += 1
        except Exception as ex:
            print(f"  ✗ {key}: {ex}")

    print(f"\n{'[DRY RUN] ' if dry_run else ''}Готово: {ok}/{len(ENTRIES)} записей.")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Расширение базы знаний UBT OS — ниши + мультиаккаунтинг")
    parser.add_argument("--dry-run", action="store_true", help="Не писать в БД")
    args = parser.parse_args()

    try:
        from dotenv import load_dotenv
        load_dotenv()
    except ImportError:
        pass

    if not os.getenv("SUPABASE_URL"):
        print("❌ Нужны переменные: SUPABASE_URL, SUPABASE_SERVICE_KEY")
        sys.exit(1)

    run(dry_run=args.dry_run)
