"""
deploy/seed_kb_content.py — Расширение базы знаний UBT OS.

Контентная часть (C1-C6) — как делать сам контент:
  - hooks       — хуки и удержание
  - format      — форматы 2025
  - copywriting — сценарии и копирайтинг
  - humanize    — стоп-слоп / уход от AI-маркеров (усиливает A19)
  - trends      — звуки/хэштеги/тайминг (усиливает A32)
  - production  — производство через нейросети (усиливает A30/A34/A35)
  - content     — сводный чеклист

Аддитивный: не трогает записи прежних сидов.
Каждая запись пишется через delete+insert по entry_key.

Запуск:
    python deploy/seed_kb_content.py
    python deploy/seed_kb_content.py --dry-run
"""
from __future__ import annotations
import argparse, os, sys

ENTRIES: list[dict] = []


def _e(key: str, title: str, content: str, tags: list[str] | None = None) -> None:
    parts = key.split(".")
    while len(parts) < 4:
        parts.append("any")
    process, platform, vertical, scheme = parts[:4]
    ENTRIES.append({
        "entry_key":  key,
        "title":      title,
        "content":    content.strip(),
        "category":   process,
        "vertical":   vertical if vertical != "any" else None,
        "tags":       (tags or []) + [process, platform, vertical, scheme],
        "version":    1,
        "is_current": True,
        "changed_by": "seed_kb_content",
    })


