_e("hooks.any.any.white",
   "Хуки · 5 типов + удержание (окно 3 сек)",
"""
## Окно решения
Алгоритм принимает решение на ~1.5 сек. Полный хук должен раскрыться до 3-й
секунды. 90% провальных роликов проваливаются в первые 3 секунды.

## 5 типов хуков (данные по 34635 роликам)
| Тип | Пример | Эффект |
|-----|--------|--------|
| Результат/продукт (лучший) | результат в первом кадре, «до/после 30 дней» | 6037 ср. просмотров |
| Эксперт-объяснение | «12 лет в [сфере], вот чего вам не говорят» | 2626 клипов |
| Контрарный | «Все говорят X. Это неверно» / «Хватит делать Y» | мозг не терпит противоречия |
| Конкретное число | «Потратил $847», «Проанализировали 34635 роликов» | сигнал достоверности |
| Императив-команда | «Стоп-скролл», «Смотри до конца» | только для важного |

## Retention-техники
- Вести с развязки, не с завязки
- Никаких «Всем привет, сегодня мы...» и преамбул
- Сигнал успеха алгоритму: зритель остался после 1.5 сек
- Хуки с переходами (transition) = +40-60% досмотров

## Банк RU-хуков (шаблоны)
- «Никто вам этого не говорил про [тему]»
- «Если ты это видишь — это знак»
- «Не поверишь, но...»
- «Вот как я добился [результат] без [затрат]»
- «Хватит уже делать [ошибку]»
- «Досмотри до конца, чтобы узнать...»
- «3 симптома/признака, которые нельзя игнорировать»
""")

_e("hooks.tiktok.any.white",
   "Хуки · Оптимальная длина видео по платформам",
"""
## Оптимальная длина (2025)

### TikTok
- 10-15 сек — трендовые клипы
- 60-180 сек — сторителлинг, value-контент

### Instagram Reels
- 15-30 сек — виральность, повторы, распространение
- 60-90 сек — вовлечение, высокий completion rate
- (лимит расширен до 3 мин в янв 2025, но оптимум короче)

### YouTube Shorts
- ~13 сек — высокий completion + петля (loop)
- до 60 сек — полная мини-история
- 3-мин опция экспериментальна, топ-контент <1 мин

## Универсально
Сильный хук в первые 3 сек критичен независимо от длины.
7-15 сек + трендовый звук = полные досмотры и повторы (алгоритм любит).
Тестировать несколько длин в этих диапазонах.
""")


