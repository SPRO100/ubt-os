"""
deploy/seed_kb.py — Сид базы знаний UBT OS.

Заполняет kb_entries профессиональными знаниями по каждой комбинации:
  process × platform × vertical × scheme

Запуск:
    python deploy/seed_kb.py
    python deploy/seed_kb.py --dry-run   # только показать, не писать в БД
    python deploy/seed_kb.py --reset     # удалить старые seed-записи перед вставкой
"""
from __future__ import annotations
import argparse, os, sys
from datetime import datetime, timezone

# ---------------------------------------------------------------------------
# Записи знаний
# ---------------------------------------------------------------------------

ENTRIES: list[dict] = []


def _e(key: str, title: str, content: str, tags: list[str] | None = None) -> None:
    parts = key.split(".")
    while len(parts) < 4:
        parts.append("any")
    process, platform, vertical, scheme = parts[:4]
    ENTRIES.append({
        "entry_key":  key,
        "title":      title,
        "content":    content.strip(),
        "category":   process,
        "vertical":   vertical if vertical != "any" else None,
        "tags":       (tags or []) + [process, platform, vertical, scheme],
        "version":    1,
        "is_current": True,
        "changed_by": "seed_kb",
    })


# ===========================================================================
# CONTENT — производство контента
# ===========================================================================

