"""
deploy/seed_kb_affiliate.py — Расширение базы знаний UBT OS.

Блок A (партнёрские программы) — ресёрч по вертикалям RU/СНГ + Tier-1/2:
  - affiliate      — карта CPA-сетей по каждой вертикали
  - vertical_guide — как делать материал под вертикаль × платформу
  - compliance     — что разрешено/запрещено по платформам
  - funnel         — рабочие воронки
  - benchmarks     — реальные ставки, конверсии, минбюджеты

Аддитивный: не трогает записи из seed_kb.py. Каждая запись пишется
через delete+insert по entry_key (как в seed_kb.py).

Запуск:
    python deploy/seed_kb_affiliate.py
    python deploy/seed_kb_affiliate.py --dry-run
"""
from __future__ import annotations
import argparse, os, sys

ENTRIES: list[dict] = []


def _e(key: str, title: str, content: str, tags: list[str] | None = None) -> None:
    parts = key.split(".")
    while len(parts) < 4:
        parts.append("any")
    process, platform, vertical, scheme = parts[:4]
    ENTRIES.append({
        "entry_key":  key,
        "title":      title,
        "content":    content.strip(),
        "category":   process,
        "vertical":   vertical if vertical != "any" else None,
        "tags":       (tags or []) + [process, platform, vertical, scheme],
        "version":    1,
        "is_current": True,
        "changed_by": "seed_kb_affiliate",
    })


